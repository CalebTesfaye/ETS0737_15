# Joining a list of strings with a comma and space
words = ["shay", "buna", "papaya"]
result = ", ".join(words)
print(result)  # Output: shay, buna, papaya

# Joining a tuple of strings with a hyphen
numbers = ("1", "2", "3", "4")
result = "-".join(numbers)
print(result)  # Output: 1-2-3-4

# Joining a set of strings with a pipe symbol
fruits = {"apple", "banana", "cherry"}
result = "|".join(fruits)
print(result)  # Output: apple|banana|cherry