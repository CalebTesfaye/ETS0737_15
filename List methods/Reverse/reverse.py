colors= ["red", "green", "blue", "yellow", "purple", "orange"]
print("Original list:", colors)
colors.reverse()
print("Reversed list:", colors)    
# Output: Original list: ['red', 'green', 'blue', 'yellow', 'purple', 'orange']
# Reversed list: ['orange', 'purple', 'yellow', 'blue', 'green', 'red']